<h1>Ýmsar Staðreyndir</h1>
<p><i>Cavia</i> er ættkvísl af naggdýrum frá Suður-Ameríku, allir kallaðir naggrísir af ýmsum toga, en "Naggrísinn" (<i>Cavia porcellus) </i>finnst ekki villtur í náttúrunni. Þeir koma upprunalega frá Andesfjöllum og hafa verið ræktaðir til manneldis á þeim slóðum síðan minnsta kosti 5000 árum fyrir Krist, og gera það enn. Það var ekki fyrr en fyrir rúmum 450 árum sem naggrísinn kom til Evrópu á skipum kaupmanna sem framandi gæludýr en elstu ritin sem nefna naggrísi eru síðan 1547. Enginn veit af hverju þeir voru síðan kallaðir "guinea pig" en giskað er að þeir voru seldir fyrir eitt guinea (gullpeningur) og fræði heitið þeirra <i>Porcellus</i> er latneska fyrir "lítill grís".</p>


<h3>Nokkrar staðreyndir um naggrísi</h3>
<ul>
	<li>Naggrísir eru yfirleitt <i>mest á hreyfingu á morgnanna og við sólsetur</i> (þegar rándýr væru ólíklegri til að taka eftir þeim).</li>
	<li>Þeir eiga til að <i>tísta þegar þeir heyra hljóð sem þeir tengja við mat</i> (t.d opna ískápinn, skrjáf í plastpokum, opna ílát)</li>
	<li>Naggrísir <i>borða sinn eiginn skít, það er fullkomlega eðlilegt</i> (álíka og jórturdýr "æla" matnum aftur í munn til að tyggja aftur).</li>
	<li>Naggrísir eru <i>mjög forvitnir</i>, sérstaklega þegar breytingar verða í búrinu eða settir í nýtt umhverfi.</li>
	<li>Naggrísir hafa venjulega mjög einfaldar þarfir og <i>rólegir að eðlisfari</i>.</li>
	<li><i>Þeir treysta mest á lyktarskynið og heyrn</i>, sjónin þeirra er aðalega til að nema hreyfingar og hafa lélegt dýptarskyn</li>
	<li>Naggrísir hafa mjög gott af félagskap (annara naggrísa og fólks). </li>
	<li>Þeir geta auðveldlega <i>vanist því að vera meðhöndlaðir</i>,  ólíklegir til að bíta en það getur gerst.</li>
	<li><i>Þeir dafna best í hópum</i>, tveir eða fleirri naggrísir saman (búrið þarf auðvitað að vera hæfilega stórt líka)</li>
	<li>Naggrísir eru <i>ekki liðugir og lélegir að klifra</i>, en þeir geta hoppað yfir lágar hindranir</li>
	<li>Þegar naggrísir eru spenntir þá hoppa þeir á ákveðinn hátt, kallast að "<i>poppa</i>" (popcorning).</li>
	<li>Þeir leysa sín vandamál og þrautir með því að hreyfa sig og skoða um, og <i>geta munað flóknar leiðir að mat í marga mánuði</i>.</li>
	<li>Naggrísir eru alveg <i>furðulega góðir í sundi</i> (<b>til að bjarga lífi sínu, óvíst að þeir geri það af sjálfsdáðum</b>)</li>
	<li>Naggrísir kallast líka "marsvín" hér í norðurlöndunum, en það er <i>einnig samnefni fyrir grindhvali</i>.</li>
</ul>

<h3>Týpur af Naggrísum</h3>
<p>Útlitið á feldinum á þeim getur verið margskonar alveg eins og persónuleikinn þeirra, en þetta er allt sama tegundin. Fólk hefur gefið þessum mismunandi týpum sérstök nöfn til að aðgreina þau og það eru jafnvel til keppnir/sýningar sem snúast algjörlega um feldinn á naggrísunum. Öll þessi afbrigði hafa orðið til vegna breytinga í genum og svo hefur mannfólkið stýrt þróun þeirra.</p>

<p><i>Ég er sjálf ekki með á hreinu hvaða týpur eru í boði hér á landi, en rósettur og slétthærðir eru algengastir.</i></p>

