<h3>Almennar upplýsingar</h3>
	<ul>
	<li><b>Meðal lífstíð naggrísa:</b> 4 - 8 ár</li>
	<li><b>Líkamshiti:</b> ~37°C til ~39°C </li>
	<li><b>Þæginlegur umhverfishiti:</b> 19°C - 22°C (ath. þeir þola betur lægri hitastig en of háan hita)</li>
	<li><b>Þolanlegur umhverfishit:</b>  15°C - 18°C</li>
	<li><b>Hjartsláttur:</b> 240 - 350 slög á mínútu</li>
	<li><b>Andardráttur:</b> 40 - 150 á mínútu</li>
	<li><b>Fullorðin líkamsþyngd:</b> 700g - 1200g</li>	
	<li><b>Þyngd við fæðingu:</b> 60g - 110g</li>
	<li><b>Fullorðin líkamslengd:</b> 25cm - 36cm</li>
	<li><b>Tilvalinn aldur/þyngd kvenndýrs til æxlunar:</b> 6 mánaða, >750g</li>
	<li><b>Meðganga:</b> 59 - 72 dagar</li>
	<li><b>Fæðing:</b> 10-30 mínútur, oftast um miðja nóttu</li>
	<li><b>Ungahópur:</b> 1-6 ungar</li>
	<li><b>Mjólkur-tímabilið:</b> 2 vikur, ætti að byrja strax eftir fæðinguna</li>
	<li><b>Hætta á spena:</b> 1-3 vikna (ungar þurfa vera orðnir minnst 170g)</li>
	<li><b>Tennur:</b> 20 talsins, sem hætta aldrei að vaxa</li>
	<li><b>Mataræði:</b> eingöngu grasætur (grænmetisætur)</li>
	<li><b>Heyrn:</b> betri en manns-heyrn (sérstaklega á hærri tíðnum)</li>
	<li><b>Sjón:</b> Sjá í lit, 340° sjónsvið, lélegt dýptarskyn, 33 myndir á sek. (mannverur sjá 22 á sek.)</li>
	<li><b>Lyktarskyn:</b> mjög þróað, einhverstaðar á milli mannveru og hunda</li>
	<li><b>Bragðskyn:</b> mjög mikið af bragðkirtlum: um 17000 (mannverur hafa um 9000, kettir um 473)</li>
	<li><b>Skynfæri á nefi:</b> 6 raðir af óreglulega staðsettum skynhárum til að leiðbeina þeim í myrkrinu, mæla hluti. Þeir sjá ekki með augunum hvað er beint fyrir framan nefið á þeim</li>
	</ul>

