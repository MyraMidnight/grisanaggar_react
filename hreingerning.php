<h1>Hreinlæti og Þrif</h1>
<p><b>Hversu oft þarf ég að þrífa búrið?</b> Þú getur hlustað á nefið á þér, það á ekki að lykta illa í búrinu. Venjulega þarf að taka búrið alveg í gegn lámark einu sinni í viku. Flestir eigendur yfirfara búrið fljótlega á hverjum degi, aðrir skipta alveg um undirlag á 4 daga fresti, handklæði þurfa stundum að vera skipt út daglega. En það veltur alveg á þinni aðstöðu hversu oft þú þarft að þrífa búrið:</p>
<ul>
<li>Hvernig undirlagi þú notar</li>
<li>Stærðin á búrinu (stærra er betra)</li>
<li>Fjöldi naggrísa í búrinu</li>
</ul>
<p>Regluleg hreingerning heldur naggrísunum þínum heilbrigðum. Ef þú þrífur oftar þá kemur þú í veg fyrir að þvagið í undirlaginu brotni niður í ammóníu, sem fer ekki vel með öndunarfæri dýranna. Auk þess þá kemur það í veg fyrir vöxt á bakteríum, sem minkar líkur á því að naggrísirnir fái t.d. þvagfærasýkingu eða aðrar sýkingar.</p>
<hr>
<h3>Edik í hreingerningar</h3>
	<p>Edik (vinegar) er mjög gagnlegt í þrifum í kringum naggrísina. Það er ekki einungis öflugt gegn bakteríum, það er líka sýra. Þar sem þvag er basískt þá vinnur edik einstaklega vel til að hreinsa burt hlandstein sem safnast up í botninum á búrinu.</p>
	<ul>
	<li><b>Það fjarlægjir vonda lykt </b>(mörg hreinsiefni bara fela lyktina)</li>
	<li>Það hefur sótthreinsandi eiginleika (öflugt gegn bakteríum)
	<li>Það kemur í veg fyrir að kalk/hland-skán myndist ef notuð reglulega</li>
	<li>Það leysir upp kalk/hland-skán ef látið liggja á blettunum</li>
	<li>Það getur mýkt upp þvottinn þinn ef þú bætir því í skol-ferlið og fjarlægjir sápulykt</li>
	</ul>

<h3>Hreinsa vatnsflöskuna</h3>
	<p>Gott er að hrista flöskuna annað slagið yfir vaskinum með smá krafti svo kúlan innan í stúts-rörinu fari á ferð, vegna þess að naggrísir eiga það til að troða matarleifum inn í stútinn og stífla hana þannig.</p>
	<p>Síðan er hérna gott ráð til að þrífa skítuga flösku að innan þegar skán er farin að myndast og erfitt er að hreinsa það:</p>
	<ol>
	<li>Takið smávegis af þurrum hrísgrjónum og setið í flöskuna</li>
	<li>Setjið smá-slatta af vatn í flöskuna</li>
	<li>Haldið fyrir stútinn með lófanum og hristið rækilega
		<ul>
		<li>Þú getur skipt um vatn í flöskunni þegar það er gruggugt, bara ekki hella hrísgrjónunum úr</li>
		</ul>
	</li>
	<li>Þegar allt er búið að losna úr flöskunni, þá getur þú hellt öllu úr og skolað flöskuna</li>
	</ol>


<h3>Þrífa kalk/hland-stein úr klósethornum og búrbotnum</h3>
	<p>Gott er að gera þetta inn á baðherbeki eða einhverstaðar sem þú getur síðan spúlað botninn með vatni eftir á</p>
	<ol>
	<li>Tæmið botninn, takið allt undirlag úr því</li>
	<li>Hellið slatta af edik í botninn svo það nái yfir blettina
		<ul>
		<li>ef þetta er búrbotninn, þá getur þú hallað honum og gert einn endan í einu</li>
		</ul>
	</li>
	<li>Láttu þetta liggja í 10 mínútur eða lengur, edikið mun leysa upp skánina
		<ul>
		<li>Getur athugað stöðuna með því að skrúbba aðeins yfir blettina og sjá hvort þeir séu enþá harðir</li>
		</ul>
	</li>
	<li>Þegar allt er laust þá getur þú einfaldlega hellt edikinu úr og spúlað botninn með vatni</li>
	<li>Nú ætti botninn/klósetthornið að líta út eins og glænýtt</li>
	</ol>

<h3>Losa hár úr flísefni (eða öðrum efnum)</h3>
	<p>Það er best að fjarlægja eins mikið af hárunum úr efnum áður en þau fara í þvottavélina.</p>
	<ol>
	<li>Vertu með bala af vatni ofan í baðkerinu/sturtunni. 
		<ul>
		<li>Vatnið þarf ekki að vera heitt, en volgt er þæginlegt þegar þú ætlar að nota hendurnar í því.</li>
		<li><b>Best er að hafa sérstakt sigti yfir niðurfallinu til að grípa hárin sem losna</b>, þau finnast t.d. í Byko</li>
		</ul>
	</li>
	<li>Settu efnið alveg í kaf ofan í balanum og hristu örlítið í því til að losa hárin
		<ul>
		<li>Hárin vilja losna úr blautu efninu og fljóta upp á yfirborðið</li>
		<li>Gott er að hafa sturtu-hausinn ofan í balanum svo hreyfing sé á vatninu, hárin fara í burtu með vatninu sem flæðir út fyrir</li>
		</ul>
	</li>
	<li>blautar hendur eru alveg furðulega góðar til að ná hárum úr flísefni: 
		<ol>
		<li>Taktu blautt flísefnið úr balanum</li>
		<li>Strjúktu blautum höndum yfir efnið, hárin vilja loða við hendina á þér</li>
		<li>Skolaðu hendurnar í vatns-balanum, og svo haldið áfram að strjúka yfir efnið</li>
		</ol>
	</li>
	<li>Þegar ekki nást fleirri hár úr efninu, þá getur þú skellt þessu i þvottavélina</li>
	</ol>
