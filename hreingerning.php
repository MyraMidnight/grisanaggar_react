<h1>Þrífum búrið</h1>
<p>Þetta er hvernig ég hef verið að þrífa búrið mitt sjálf. Ég reyni að gera þetta sem einfaldast, ef það tekur stuttan tíma að taka búrið í gegn þá erum við ekki að trufla naggrísina eins mikið. </p>
<p>Best er að gera þrifin á svipuðum tíma dags, naggrísir eru mjög vanafastir og frekar fljótir að læra þekkja dagsrkánna. Sjálf geri ég þetta á kvöldin þegar allt er rólegt, og gef þeim síðan skammt af grænmeti þegar ég er búin.</p>
	<ul>
	<li>Ef búrið þitt er lítið þá er best að færa naggrísina á tímabundið svæði þar til búið er að yfirfara búrið. Skalt hafa það einhvað sem þær meiga pissa á svo þær þurfa ekki að halda í sér</li>
	<li>Mínir naggrísir annað hvort flýja á svæði sem ég er ekki að þrífa, eða fara labba í kringum fægiskúffuna meðan ég er að vinna eins og þau séu að sanna hversu djarfar þær geta verið</li>
	</ul>
<p>Naggrísir gera mestar þarfir sínar á meðan þær eru að borða, þess vegna er oftast mesti skíturinn nálægt heygrindinni. <br><b>Þegar þú ert með flísefni</b>: Það er þæginlegt að hafa afmarkað matarsvæði undir heygrindinni fyllt með sagi eða öðru undirlagi, þá helst restin af búrinu lengur hreint og þarft þá ekki að taka allt búrið í gegn eins oft. Þetta getur verið gert úr lágum plastkassa sem þær geta hoppað ofan í, eða skorið glugga í box með hærri hliðum svo þær komist inn á matarsvæðið </p>

<h3>Dagleg þrif</h3>
<ol>
<li>Fyrst tek ég dótið af svæðinu sem ég er að fara þrífa</li>
<li>Síðan tek ég fægiskúffuna mína og tek það helsta
	<ul>
	<li><b>Í sagkassanum:</b> Ég tek skítahauginn sem myndast alltaf í vinsælu hornunum, hendi heyinu sem þau hafa togað úr grindinni (ef þeir toga of mikið þá þarftu kanski þéttari heygrind). Ef pissuhornin eru orðin frekar dökk þá er tími til að taka sagkassann í gegn.</li>
	<li><b>Á flísefninu:</b> 
		<ol>
		<li>Ég sópa upp öllum skít og sagi sem hefur safnast á yfirborðinu.</li>
		<li>Næst athuga ég helstu pissuhorning hvort skipta þurfi um handklæði. Legðu lófan á yfirborðið á flísefninu, ef þú finnur rakann í gegnum það þá er tími til að skipta um handklæði. <br>Þú getur skipt um handklæðið án þess að þurfa skipta um flísefnið, vegna þess að það getur alveg beðið þar til í lok vikunnar.</li>
		</ol>
	</li>
	</ul>
</li>
<li>Þegar öllu er lokið, þá skila ég dótinu þeirra og þríf á mér hendurnar auðvitað</li>
</ol>

<h3>Sagkassinn tekinn í gegn</h3>
<ol>
<li>Fyrst tek ég dótið og matarskálina úr sagkassanum</li>
<li>Sópa upp öllum skít og heyi sem eru á yfirborðinu svo það líti frekar hreint út</li>
<li>Finndu öll helstu pissuhornin, sem eru þá orðin áberandi dökk vegna hlands, og taktu það í burtu með fægiskóflunni</li>
<li>Þegar allt blautt sag er farið, þá færi ég þurra sagið á svæðinu út í öll horn og enda. Þá verður miðjan á kassanum yfirleitt tóm.
	<ul>
	<li>Þetta geri ég til að hafa smá hreyfingu á saginu</li>
	<li>Þú skalt tæma kassan alveg annað slagið og þrífa botninn með edik </li>
	</ul>
</li>
<li>Síðan tek ég nýtt sag og filli upp í kassan (hafðu þetta nokkra sentimetra þykkt lag af sagi)</li>
<li>Sniðugt er að klára dæmið með því að reyna þjappa saginu niður í kassan aðeins, það hjálpar að minka hversu mikið það dreifist um búrið ef þú notar flísefni líka</li>
</ol>

<h3>Flísefnið tekið í gegn</h3>
<p>Þú skalt hafa auka sett af undirlagi og flísefni til skiptana svo naggrísirnir þurfi ekki að bíða þvottinum</p>
<ol>
<li>Fyrst tek ég dótið úr búrinu</li>
<li>Taktu fægiskúffu og sópaðu upp öllu lauslegu ofan af flísefninu</li>
<li>Dragðu handklæðið undan flísefninu, ég læt það bíða í þvottavélinni þar til ég get sett flísefnið með</li>
<li>Reyndu að ná restinni af kuskinu af flísefninu, búrbotninn getur gripið það.
	<ul>
	<li>Liftu upp flísefninu og sópaðu kuskið niður í búrbotninn</li>
	<li>Þú getur síðan farið með flísefnið út í garð/svalir og hrist það rækilega í lokin</li>
	</ul>
</li>
<li>Þegar allt kusk er farið úr efninu, þá getur þú skellt því í þvottavélina með handklæðinu
	<ul>
	<li>Ef það er mikið af hárum í flísefninu, þá getur þú prufað að drekkja því í bala af vatni fyrst. Útskýringar eru hér ofar á síðunni</li>
	<li>Yfirleitt set ég þvottinn á 60°C, Neutral þvottaduft (einhvað sem er fyrir viðkvæma húð og án ylmefna). </li>
	<li><b>Ekki nota mýkingarefni!</b> en sniðugt er að setja edik í hólfið í staðinn, það mun fjarlægja vonda lykt og fleirra</li>
	<li>Þú getur slept því að nota þvottaefni og bara skvett slatta af edik út í þvottinn, það virkar gegn þvaginu.</li>
	</ul>
</li>
<li>Gerðu búrið tilbúið á meðan þú bíður eftir vélinni
	<ol>
	<li>Ryksugaðu kuskið úr búrbotninum</li>
	<li>Strjúktu yfir botninn með edik</li>
	<li>Settu nýtt undirlag og flísefni í botninn</li>
	<li>Skilaðu dótinu í búrið og naggrísunum</li>
	</ol>
</li>
<li>Þegar þvottavélin er búin:
	<ul>
	<li><b>Hristu rækilega úr efninu til að ná mest af hárunum úr því</b>. Best væri að gera þetta úti í garði/svölunum og á meðan efnið er ennþá rakt. Mest af hárunum eiga það til að stökkva úr flísefninu yfir í handklæðið á meðan það er í þvottinum.</li>
	<li>Stundum hristi ég úr þessu yfir baðkerinu ef ég get ekki notað svalirnar og ryksuga upp draslið sem dettur af því</li>
	<li>Þú mátt setja þetta í þurrkara, en þú skalt þá taka flísefnið snemma úr vélinni, og passa upp á það að filterinn fyllist ekki af hárum (stundum þarf að tæma filterinn inn á milli ef mikið var af hárum). <i>Alls ekki nota nein þurrku-blöð eða önnur efni sem mýkja upp þvott í þurrkara</i></li>
	</ul>
</li>
</ol>
