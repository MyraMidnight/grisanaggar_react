<p>Venjulega er einföld meðferð alveg nóg ef þú sótthreinsar búrið og dótið þeirra vel svo lúsin geti ekki hoppað aftur á naggrísinn að meðferðinni lokið. Ef endurtaka þarf meðferðina, þá er það venjulega gert 4 vikum síðar. Meðferðin sjálf er mjög einföld og eru nokkrir dropar af <i>Selametchin</i> á hnakkan á þeim/bak við eyrun. Dót sem erfitt er að sótthreinsa er einfaldlega hægt að henda í frystirinn. </p>

