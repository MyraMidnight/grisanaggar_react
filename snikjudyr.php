<h2>Kláðamaur, <i><a href="http://www.guinealynx.info/mites.html">Mange mites</a> (Trixacarus caviae)</i></h2>
<p>Þessi pest sést ekki með berum augum og grefur sig inn í húðina á naggrísnum sem veldur þeim miklum sársauka. Þú gætir tekið eftir því að naggrísinn klóri sér mikið og fær skallabletti, í alvarlegum tilfellum getur naggrísinn fengið krampa og jafnvel dáið ef ekki meðhöndlað. <b>Ef einn naggrísinn hefur kláðamaur, þá skaltu gera ráð fyrir að allur hópurinn hafi það</b>, jafnvel ef hinir sýna engin einkenni.</p>

<p>Naggrísir geta verið einkennalausir og samt verið með kláðamaur í sér, maurinn getur nefnilega legið í dvala í marga mánuði eða ár. Mikið stress, veikindi eða þungun getur valdið því að maurarnir blossi upp.</p>

<p><b>Þessi kláðamaur lifir og fjölgar sér eingöngu á naggrísum</b> og þess vegna er engin hætta á því að þeir smitist yfir á mannfólk. En viðkvæmt fólk gæti samt fundið fyrir tímabundnum kláða.</p>

<p>Algengt er að nota <i>ivermectin</i> gegn þessu sníkjudýri í sprautu, munnlega eða á skinnið (þá á bakvið eyrun). <b>Það þarf að endurtaka meðferðina gegn kláðamaurnum tvisvar eða oftar á 7-10 daga fresti vegna þess að lyfið drepur ekki eggin.</b> Mjög mikilvægt er að skammta lyfjunum rétt til þess að forðast aukaverkanir og jafnvel dauða af völdum lyfjanna. Dýralæknirinn gæti viljað setja lyfið í opin sár, hrúður eða önnur sýnileg einkenni af völdum kláðamaursins.</p>

<p>Dauði af völdum kláðamaurs er útfrá vökvaskorts vegna mikilla sára og átröskunnar vegna sársauka og óþæginda.</p>

