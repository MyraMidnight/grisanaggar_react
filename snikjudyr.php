<p>Öruggasta leiðin til að vernda naggrísahópinn þinn gegn smitsjúkdómum, sveppasýkingum og sníkjudýrum er að <b>einangra nýja meðlimi í 2-4 vikur í lokuðu herbeki</b> áður en þeir fá að kynnast restinni af hópnum. Þvoðu þér vel um hendurnar eftir meðhöndlun á nýja naggrísnum og mögulega skipta um föt (eða nota t.d. svuntu eða ákveðin föt) til að koma í veg fyrir smit. <a href="dyraspitalar.php">Þú getur líka íhugað að kíkja til dýralæknis með naggrísinn í skoðun.</a></p>
