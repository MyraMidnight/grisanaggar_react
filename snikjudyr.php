<h2>Flösumaur / Naggrísalús, <i><a href="http://www.guinealynx.info/lice.html">Running-lice</a></i></h2>
<p>Flösumaurinn er eitt algengasta snýkjudýrið sem finnst á naggrísum, sérstaklega ef þeir koma úr dýrabúð eða frá ræktanda. <b>En hafðu í huga að naggrísinn getur verið nánast einkennalaus</b> ef lúsin er ekki að yfirtaka hann svo það er mikilvægt að skoða þá mjög vel.</p> 

	<ul>
	<li>Þeir eru þekktir hérlendis sem "<i>flösu-maur</i>" vegna þess að þeir minna helst á flösu, sérstaklega eggin þeirra.</li>
	<li>Á ensku eru þeir kallaðir "<i>Running Lice</i>" (hlaupa-lús) vegna þess hreyfa sig mikið.</li>
	<li>Þeir eru sérhæfðir til að lifa eingöngu á naggrísum svo það er engin hætta á því að þeir smitist á mannfólk. </li>
	<li>Þeir drekka ekki blóð, svo lyf sem naggrísinn fær munnlega virka ekki á þá</li>
	<li><b>Smitleiðir:</b>
		<ul>
		<li>Bein snerting við smitaðann naggrís</li>
		<li>Snerting við undirlag/yfirborð/leikföng sem smitaður naggrís hefur verið í snertingu við</li>
		<li>Frá manneskju sem var að meðhöndla smitaðann naggrís og fengið eggin/lúsina í fötin</li>
		</ul>
	</li>
