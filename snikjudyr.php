	<ul>
	<li>Naggrísinn virðist heilbrigður, ættleiddur frá öðrum eiganda sem hefur nú þegar meðhöndlað naggrísinn gegn sníkjudýrum og hefur ekki verið í snertingu við aðra naggrísi undanfarnar vikur.</li>
