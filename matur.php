<h3><a name="cvitamin">C-vítamín</a></h3>
<p>Naggrísir framleiða ekki eigið C-vítamín og þurfa því að fá það úr mataræðinu. Yfirleitt fá þeir sinn skammt af því í gegnum ferskt grænmeti og þurrmat sem hefur viðbætt c-vítamín, en stundum þarf að gefa þeim það sérstaklega í formi tyggjanlegrar töflu (<i>1/4 af töflu</i>) eða fljótandi í dropateljara.</p>
<ul>
	<ul>
		<li><b>Þeir þurfa 10-30 mg/kg daglega</b> til að koma í veg fyrir <i>skyrbjúg</i> (scurvy).
			<ul>
				<li>Í flestum tilfellum er nóg að gefa þeim ferskt grænmeti og þurrmat með viðbættu C-vítamíni</li>
			</ul>
		</li>
		<li>Naggrísir sem eru ungir, veikir, óléttir eða nýbúnir að eignast unga þurfa auka skammt af C-vítamíni.</li>
		<li><b>Ekki setja C-vítamín í vatnið hjá þeim: </b>
			<ul>
				<li>Hreint vatn er mikilvægt handa heilsu þeirra, en vítamínið breytir bragðinu á því og getur fengið grísina til að drekka minna.</li>
				<li>Það er fljótt að missa virkni sína út í vatninu</li>
				<li>Naggrísir drekka mis-mikið svo það er ómögulegt að vita hversu mikið vítamín þeir væru að fá</li>
			</ul>
		</li>
	</ul>
</ul>
<h3><a name="vatn">Vatn</a></h3>
<p>Naggrísir þurfa að hafa aðgang að hreinu vatni, helst í vatnsflösku með stút hreinlætisins vegna. Ef þú notar skál með vatni þá eiga naggrísirnir auðvelt með að menga vatnið þegar skítur dettur ofan í það. Best er að hafa flöskuna í þeirri hæð að þeir þurfa lyfta hausnum til að ná í stútinn.</p>
<ul>
<ul>
	<li>Hreint vatn skal alltaf vera til staðar</li>
	<li>Gefið þeim hreint kalt vatn daglega, jafnvel ef þeir hafa ekki klárað úr flöskunni</li>
	<li>Ekki setja vítamín eða meðöl úti í drykkjarvatnið þeirra</li>
	<li>Gott er að setja flöskuna nógu hátt svo að naggrísinn þarf að lyfta hausnum, eins lengi og hann þarf ekki að lyfta framfótunum af jörðinni. Ég hef séð það hjá mínum naggrísum að ef flaskan er lægra niðri þá fara þær oft að snúa höfðunu óþæginlega til að fá sér sopa.</li>
	<li>Þeir fá líka eithvað af vatni úr fersku grænmeti, svo þeir gætu þar af leiðandi drukkið minna vatn. Það er allt í lagi.</li>
</ul>
</ul>

<h3><a name="hey">Hey/Gras</a></h3>
<p>Naggrísir eru grasætur og þurfa því alltaf að hafa aðgang að góðu heyi/grasi, sem á að vera 80% af mataræðinu þeirra.</p>
<ul>
	<ul>
		<li>Heyið skal vera vel þurrt, því það myglar auðveldlega ef raki myndast</li>
		<li><b>Gott hey er grænt hey</b>, gult hefur enga næringu í því lengur</li>
		<li>Hafið einvherskonar hey-grind í búrinu svo heyið sé ekki á jörðinni,það kemur í veg fyrir að naggrísirnir pissi í það og haldist þurrt.</li>
		<li>Naggrísir hafa oftast lítinn áhuga á heyi sem þeir hafa togað niður úr heygrindinni. Ættir að henda því úr búrinu reglulega.</li>
		<li>Ferskt gras úr garðinum er auðvitað betra en þurrkað hey þegar færi gefst
			<ul>
				<li>Ef verið er að skipta yfir í ferskt gras er best að gefa þeim lítið í einu fyrst, ekki taka heyið frá þeim strax, svo þeir fái ekki meltingar-truflanir.</li>
				<li><span style="color: #ff0000;">Forðist að safna grasi sem vex nálægt mikilli umferð eða þar sem hundar pissa og <b>alls ekki þar sem þeir úða skordýraeitri</b></span></li>
				<li>Stráin eru svo næringarsnauð að þú ættir ekki að safna þeim til að gefa naggrísunum (<i>þau eru "blómin" hjá grasinu</i>)</li>
				<li>Helst ekki nota gras sem slegið er með slátturvél. Það er tætt og blautt af gras-safa. Naggrísir vilja grasið sitt lengra.</li>
				<li>Þú sparar á því að þurfa ekki að kaupa þurrt hey á sumrin og getur prufað að þurrka eigið hey sem nýtist þér eftir að grasið hættir að vaxa. En ef þú þurrkar það ekki algjörlega þá myglar það bara. Samt gaman að gera tilraunir.</li>
				<li>Auðvelt er að rækta ferskt gras til að gefa þeim um veturinn. Það er betur þekkt sem "<b>hveitigras</b>" og vex af hveitikornum.</li>
			</ul>
		</li>
		<li><i>AlfaAlfa</i> er mjög næringarrík tegund af grasi, en það i<i>nniheldur einnig mikið af kalki</i> svo ekki nota það í stað venjulegs heys. Hægt er að gefa það ungum, óléttum og undir-nærðum naggrísum og ef þeir voru að eignast unga. Fullorðnir naggrísir gagnast ekki af þessu auka magni af kalki og ætti því ekki að fá það oft.</li>
	</ul>
</ul>

<h3><a name="þurrfóður">Þurrfóður</a></h3>
<p>Naggrísir þurfa góðan <b>þurrmat sem hefur viðbætt C-vítamín</b>. Það eru til tvær týpur af þurrfóðri: Múslí-fóður sem er blandað, og köggla-fóður þar sem allir bitarnir eru eins. <b>Þú skalt skoða innihaldið á fóðrinu áður en þú kaupir</b>, vegna þess að mörg fyrirtæki sem framleiða þurrfóður halda að þetta séu bara stórir hamstrar.</p>
<ul>
	<ul>
		<li>Hver naggrís ætti að fá<b> sirka <i>30ml</i> af þurrfóðri á dag ásamt fersku grænmeti og nóg af heyi.</b></li>
		<li><b>Múslí-fóður</b> eru blöndur með margskonar bitum
			<ul>
				<li>Naggrísir eiga það til að velja bara bestu bitana í múslí-fóðri og borða því ekki alla næringuna sem þeir ættu að fá.</li>
				<li>Forðist blöndur sem innihalda fræ og hnetur (það er "hamstra-fóður")</li>
			</ul>
		</li>
		<li><b>Köggla-fóður</b> innihalda einsleita köggla sem allir eru eins
			<ul>
				<li>köggla-fóður er gott fyrir matvanda grísi, þá geta þeir ekki verið að velja úr bitana</li>
				<li>Forðist köggla-fóður sem inniheldur dýra-afurðir eða aðalega búið til úr maís (corn).</li>
			</ul>
		</li>
		<li>Kaupið fóðrið í hæfilegu magni (ekki of mikið) og geymið á svölum, þurrum stað til að passa upp á C-vítamínið í fóðrinu</li>
		<li>Skoðið "best fyrir" dagsetninguna til að vera viss um ferskleika fóðursins</li>
		<li>Notið helst þunga keramík-skál svo þeir velti henni ekki, og þrífið daglega.</li>
	</ul>
</ul>

<h3><a name="grænmeti">Grænmeti</a></h3>
Gefið naggrísunum ferskt grænmeti daglega svo þeir fá nóg af vítamíni. Grænmetið sem hefur sterka og skæra liti hafa oftast mestu næringuna.
<ul>
	<ul>
		<li><b>Hver og einn naggrís ætti að fá</b> <b>sirka 1 bolla (~2,5 dl) af grænmeti á dag </b>
			<ul>
				<li>Sniðugt er að skipta því í minni skammta til að gefa þeim 2-3 sinnum yfir daginn</li>
				<li>Naggrísir fá góðan skammt af C-vítamíni úr fersku grænmeti</li>
				<li>Ef naggrísinn er ekki vanur fersku grænmeti, þá skaltu gefa þeim lítið til að byrja með</li>
				<li>Ekki gefa þeim niðursoðna eða þurrkaða ávexti/grænmeti</li>
			</ul>
		</li>
		<li><b>Fjölbreyttni er lykillinn að góðri heilsu</b>, nokkrar tegundir af grænmeti (og smá ávöxtum) er best
			<ul>
				<li>Ávextir ættu aðeins að vera lítill hluti af mataræðinu</li>
				<li>Ekki gefa of mikið af gulrótum – mesta lagi litla gulrót annan hvern dag eða álíka</li>
				<li>Forðist/takmarkið allt kál (t.d. hvítkál, spergilkál, rósakál, blómkál…) því það getur valdið gasi/vindverkjum</li>
				<li>Skerið sellerí í litla bita svo strengirnir í því valdi ekki vandræðum</li>
				<li>Best að forðast Jöklasalat/Iceberg vegna þess að það hefur litla næringu og getur valdið lausum/blautum hægðum í of miklu magni</li>
				<li>Forðist grænmeti með mikið af sterkju (t.d. kartöflur)
					<ul>
					<li>alls ekki gefa þeim kartöflu-skinn/augu, því þau eru frekar eitruð fyrir þá</li>
					</ul>
				</li>
			</ul>
		</li>
	</ul>
</ul>

