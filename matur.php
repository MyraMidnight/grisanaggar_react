<h3><a name="forðist">Hafið í huga:</a></h3>
	<ul>
		<li>Naggrísir sem eiga til að fá <b>nýrnasteina</b> ættu að fá takmarkað eða ekkert þurrfóður, vegna kalksins (calcium). Fylgist vel með þyngdinni á þeim.</li>
		<li> Ef þvagið hjá naggrísnum er hvítt þá eru þeir að fá of mikið af kalki í mataræðinu. Of mikið kalk getur valdið nýrnasteinum, naggrísir ættu að drekka mikið af vatni til að skola því úr líkamanum.</li>
		<li>Það er aldrei gott merki ef naggrísinn borðar ekki eðlilega. Naggrísir geta dáið úr hungri ef tennurnar á þeim brotna en þú getur bjargað þeim með því að gefa þeim matinn sem mauk í stórri sprautu (þessar sem hafa stórt op) þar til að tennurnar hafa vaxið aftur. Og auðvitað fara með grísinn til dýralæknis sem kann að meðhöndla naggrísi eða getur kynnt sér málin.</li>
	</ul>
<h3><i><b><span style="color: red;">Forðist Eftirfarandi Hluti</span></b>:</h3>
	<ul>
		<li>Forðist fóður/nammi sem innihalda <b>hnetur, fræ, þurrkaða ávexti og litarefni</b></li>
		<li>Ekki gefa þeim <b>mjólkur- eða kjöt-afurðir</b> (naggrísir eru grasætur)</li>
		<li>Forðist <b>fræ í skurn</b> (eins og sólblóma-fræ), áhætta á köfnun</li>
		<li>Ekki gefa þeim <b>kanínu-fóður</b> því það inniheldur ekki C-vítamín. Sumar tegundir geta líka innihaldið efni sem eru eitruð fyrir naggrísi.</li>
		<li>Forðist eða gefið sparlega grænmeti úr <b>kál</b>-fjölskyldunni (t.d. hvítkál, spergilkál/brokoli, kínakál) því það getur valdið gas-vandamálum og innihalda oft mikið kalk</li>
		<li>Forðist að gefa þeim Jöklasalat/<b>Iceberg</b>, því það er mjög næringar-snautt og inniheldur mikið vatn. Getur valdið niðurgangi/blautum hægðum og næringarskorti.</li>
		<li>Ekki nota <b>saltsteina</b> í búrinu þeirra</li>
		<li>Forðist<b> gæludýra-nammi sem er selt í dýrabúðum</b> merkt nagdýrum (eins og jógúrt-dropa) sem geta haft skaðleg áhrif á heilsuna hjá þeim. Með því að gefa þeim þessar óþarfa kaloríur (sumt inniheldur fitu, sykur og jafnvel auka kalk) getur látið naggrísina borða minna af öðrum mat sem þeir virkilega þurfa. Ávextir eru miklu betri sem nammi handa þeim.</li>
	</ul>
<b><span style="color: #ff0000;">Alls engin fjölvítamín!</span> </b>Einfalt C-vítamín er í lagi, en fjölvítamín eru það ekki. Of mikið af uppleisanlegum vítamínum eins og A- og D- geta valdið alvarlegum vandamálum fyrir naggrísina.

