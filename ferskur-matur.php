<td colspan=4><p><i>Nánast daglega</i> er vegna þess að þú ættir að vera gefa þeim úrval af grænmeti svo þeir fái fjölbreyttari næringu.</p>
<p>Endilega skoðið nánari næringargildi á listunum neðar á síðunni. Ef þú kynnir þér næringuna í grænmetinu þá getur þú stuðlað að því að þeir fái það magn sem þeir þurfa án þess að þurfa gefa þeim C-vítamín í t.d. töflum eða dropum. </p>
<p>Passa samt upp á jafnvægið á magni C-vítamíns og Kalks í hverju grænmeti fyrir sig. <br><a href="http://www.guinealynx.info/calculator.xls">Hér er  góð "reiknivél"</a> (Excel skjal) frá Guinea Lynx  til að reikna hlutföllin </p>
