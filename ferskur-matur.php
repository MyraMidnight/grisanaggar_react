<h2><a id="c-vitamin">C-vítamín</a></h2>
<p>C vítamín (eða "Askorbínsýra) er vatnsleysanlegt og brotnar fljótt niður svo grænmeti og ávextir innihalda minna magn en taflan sýnir eftir því sem líður frá uppskeru. 
<br>Naggrísir framleiða ekki eigið C vítamín, alveg eins og við, og þurfa þess vegna að fá það í gegnum matinn. Skortur á C vítamíni getur haft alvarlegar afleiðingar og getur þróast hratt (á nokkrum vikum). 
<br>Naggrísir sem eru ungir, veikir, óléttir eða nýbúnir að eignast unga þurfa auka skammt af C-vítamíni.</p>

