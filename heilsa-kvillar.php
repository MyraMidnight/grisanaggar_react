<h3>Ástand: Hægðartregða? (<a href="http://www.guinealynx.info/impaction.html">Impaction</a>) - tengist karldýrum</h3>
<p>	Eldri karldýr geta þróað með sér svokallaða hægðartregðu (<i>impaction</i>). Naggrísir eru með sérstakan "endaþarms-poka"<i> (perineal sac)</i> sem safnar saman mjúkum hægðum (<i>Cecal pellets). </i>Þegar vöðvarnir í endaþarminum verða slakir og geta ekki lengur ýtt sjálfir út þessum sérstöku hægðum þá pakkast þær saman í þessum poka. Eigandinn verður þess vegna að aðstoða hann reglulega með því að tæma hann.</p>
	<ul style="list-style-type: square;">
		<li>Hvernig er hægt að fyrirbyggja þessu?
	<ul>
		<li>Gott mataræði með mikið af heyi og stórt búr til að hreyfa sig í (með orku-miklum félaga) mun hjálpa við að halda karldýrinu þínu heilbrigðu. Ef undirlagið þitt (sag/pappírs-vörur) safnast saman í endaþarms-pokanum, þá gæti hjálpað að skipta yfir í flísefni.</li>
		<li>Þú ættir að skoða endaþarms-opið á naggrísunum þínum reglulega (aldur skiptir ekki máli) vegna þess að drasl á það til að safnast saman þarna t.d. hey og sag. Hreinsið ef þarf.</li>
	</ul>
	</li>
		<li>Meðferð/hreinsun:
	<ul>
		<li>Gott er að nota eyrnapinna til að fjarlægja hægðirnar úr pokanum (þegar lítið er í honum) eða reyna opna endaþarms-pokann varlega til að ýta hægðunum út (þegar mikið er í) og síðan skola hann að innan með volgu vatni úr sprautu. Gott ráð er að smyrja pokann að innan með steinefna-olíu.</li>
		<li>endilega skoðið aðrar nánari leiðbeiningar: <a href="http://www.guinealynx.info/impaction.html">Anal Impaction</a></li>
	</ul>
	</li>
	</ul>
