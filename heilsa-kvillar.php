<h3>Fótasár (<a href="http://www.guinealynx.info/pododermatitis.html">Pododermatitis/Bumblefoot</a>)</h3>
<p>
	<b><i>Ulcerative pododermatitis</i></b>, þekkt sem "bumblefoot" á ensku, er sársaukafull sýking undir fótum. Ilin er bólgin og stundum með opið sár. Í mjög alvarlegum tilfellum þá vill naggrísinn ekki hreyfa sig, verður þunglyndur og horaður. Ef sýkingin nær inn að beini þá þarf líklega að fjarlægja fótinn. Ef ekkert er gert í málunum þá getur þetta valdið dauða.
	<ul style="list-style-type: square;">
		<li><i>Hvað veldur þessu?</i>
			<ul>
				<li>Algengustu ástæðurnar eru gólf gerð úr víra-neti eða gróft/hart undirlag sem leyfir sýkingum (oftast <i>Staphylococcus aureus</i>, algeng baktería) að ná rótum. Lélegt hreinlæti, blautt undirlag, lítil hreyfing og offita geta verið orsakir. Best er að fyrirbyggja gegn þessu með því að forðast slíkar aðstæður.</li>
			</ul>
		</li>
		<li><i>Meðferð/Lækning</i>
			<ul>
				<li>Það getur verið mjög erfitt að lækna þetta. Þú skalt skoða naggrísinn þinn reglulega til að grípa svona vandamál snemma svo dýralæknir geti gefið greiningu og meðferð.</li>
			</ul>
		</li>
	</ul>
</p>
