<a name="ýmislegt"><h2>Vissir þú að...</h2></a>

<ul>
<li><b>Ávextir:</b>
	<ul>
		<li>Flestir ávaxta-steinar innihalda <i>cyanide-eitur</i> í einhverju magni, þar á meðal úr eplum, perum, kirsuberjum, ferskum, plómum og skyldum ávöxtum. Alls ekki gefa gæludýrum ávaxta-steina.</li>
		<li><i>Nektarína</i> eru nakin afbrigði af <i>ferskju</i>, sami ávöxturinn að innan</li>
		<li><i>Plómur</i> er mjög fjölbreyttur hópur, það eru t.d. græn, gul, fjólublá og rauð afbrigði</li>
		<li><i>Pluot</i> er blendingur af plómum og apríkósum</li>
		<li><i>Sveskjur</i> þurrkaðar <i>plómur</i>, yfirleitt steinlaus afbrigði</li>
		<li><i>Rúsínur</i> þurrkuð <i>vínber</i>, yfirleitt steinlaus afbrigði</li>
		<li><i>Gráfíkjur</i> er sérstakt nafn fyrir þurrkaðar <i>fíkjur</i></li>

	</ul>
</li>
<li><b>Grænmeti og Jurtir:</b>
	<ul>
		<li>"baby-carrots" eru bara venjulegar <i>Gulrætur</i> skornar niður í minni bita</li>
		<li>Laufblöðin af <i>Rauðbeðju</i> er hægt að nota eins og <i>Blaðbeðju</i>.</li>
		<li><i>Steinseljurót</i> og <i>Nípa</i> líta svo nákvæmlega eins út að þau eru oft talin sama grænmetið</li>
		<li><i>Yam</i> er rótarhnúður frá Afríku, en oft þegar fólk finnur "yam" í búðum þá eru það í raun <i>Sætar kartöflur</i></li>
		<li>"<i>Summer squash</i>" eru grasker sem eru uppskorin hálf-þroskuð, en "<i>Winter squash</i>" eru full-þroskuð grasker</li>
		<li><i>Rabarbari</i> inniheldur svo mikið af oxalsýru að hún getur haft óæskileg áhrif á nýrnastarfsemi hjá fólki.</li>
		<li><i>Papríka, Eldpipar</i> og <i>Jalapeno</i> eru nokkrar af mörgum mismunandi ræktunar-afbrigðum af sömu tegundinni <i>(Capsicum annuum)</i></li>
	</ul>
</li>
<li><b>Villtar Jurtir:</b>
	<ul>
	<li><i>Hvítsmárar</i> geta framleitt blásýru þegar særðir, en vegna þess að frost skemmir þessi efni þá er minni hætta af þeim á norðurslóðum. Sauðfé virðist ekki greina blásýruna í hvítsmára en hún er hættuleg fyrir snigla.</li>
	<li>Snerting við <i>tröllahvönn</i> að viðbættu sólarljósi veldur einskonar ofnæmi á skinni og myndast sársaukafullar blöðrur. Eftir að þær hjaðna situr eftir litarmunur á húðinni sem getur verið áberandi í mörg ár.</li>
	<li><i>Krossfífill</i> inniheldur efni sem geta valdið alvarlegum lifrarskemmdum og erlendis koma reglubundið upp dauðsföll hjá búpeningi af völdum hans.</li>
	<li>Í blómskipan <i>Hóffífils</i> eru eiturefni sem geta valdið lifrarskemmdum hjá rottum. </li>
	<li><i>Lúpínan</i> myndar eiturefni sem valdið geta lömun hjá sauðfé. Eituráhrif lúpínutegunda geta komið fram sem erfðagallar hjá afkvæmum kúa og kinda.</li>
	</ul>
</li>
</ul>

