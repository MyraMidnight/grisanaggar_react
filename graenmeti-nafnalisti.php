<h1>Nöfn á Grænmeti og Ávöxtum</h1>
<p>Það getur verið flókið að vita hvað er öruggt fyrir gæludýrið að borða þegar þú veist ekkert hvað hlutirnir heita. Ég hef alltaf haft gaman að því að þýða hluti og auk þess hef ég einhverja reynslu á grænmeti þar sem ég vann í þeirri deild í matvöruveslun í lengri tíma. Á þessari síðu getur þú fengið að sjá ensk og íslensk heiti grænmetis, ávaxta og kryddjurta sem þú gætir haft gagn af því að þekkja. Sérstaklega þegar verið er að skoða erlendar síður.</p>

<p>Þessi listi hefur verið flokkaður eftir skyldleika að mestu leiti. Þegar verið er að skoða myndirnar, þá eru þær hópaðar saman: ávextir, ber, grænmeti, kryddjurtir og viltar jurtir</p>
