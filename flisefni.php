<h3>Hvernig skal ég nota flísefni í búrinu?</h3>
<p>Þú þarft að hafa rakadrægt undirlag til að hafa undir flísefninu, gott er að nota handklæði vegna þess að það getur þá farið í þvottavélina með flísefninu. Síðan getur þú klippt og saumað flísefnið til að passa fullkomlega í búrið þitt. Það er hægt að búa til einskonar "<a href="http://www.guineapigmarket.com/about-fleece-flippers">fleece flippers</a>" sem virka mjög skemmtilega í stærri búrum og  heldur flísefninu strektu svo hægt er að ryksuga það</p>
<ol>
	<li>Vertu með hreinan búr-botn.
<ul>
	<li>Ef það eru komnir kalk/hland-steinar í botninn þá er hægt að leggja það í bleyti ofan í edik, það leysir upp skánina og búrið verður eins og nýtt.</li>
</ul>
</li>
	<li>Settu rakadrægt undirlag í botninn
