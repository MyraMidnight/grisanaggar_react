<ul style="list-style-type: square;">
	<li>Kostir
<ul>
	<li><b>Öruggt í notkun:</b> flísefnið ertir ekki viðkvæm augu naggrísa, skinn og öndunarfæri</li>
	<li><b>Fjölnota</b>: bara skella því í þvottinn reglulega. Hægt að nota fjölnota undirlag eins og handklæði með því</li>
	<li><b>Getur litið vel út</b>: flísefni getur verið mjög fjölbreytt á litinn og munstrað</li>
	<li><b>Sparar þér pening</b>: ef þú hættir að eyða í einnota undirlög þá getur þú sparað pening</li>
	<li><b>Yfirborðið helst þurrt</b>: þegar það er for-meðhöndlað rétt þá sigtar það vökva í gegnum sig, rakadrægt undirlag drekkur það í sig og naggrísirnir haldast þurrir og hreinir</li>
	<li><b>Passar auðveldlega í búrið</b>: hægt er að sauma og klippa flísefnið til að passa vel í búrið hjá þér.</li>
</ul>
</li>
	<li>Gallar
<ul>
