<h3>For-meðhöndlun flísefnis</h3>
<p>Flísefni eru venjulega vatns-fráhrindandi þegar þú færð það í hendurnar og myndar þess vegna polla ofan. Til þess að flísefnið fái þá eiginleika sem við viljum svo það sé nothæft í búrið þá þarf að for-meðhöndla flísefnið. <b>Efnið mun skreppa örlítið saman í þvottinum</b>, þess vegna er best að formehöndla áður en þú byrjar einhver saumaverkefni.</p>
<ul>
<ol>
	<li>Setjið flísefnið í þvottavélina á suðuþvott (95°) með slatta af edik <i>(og/eða þvottadufti t.d. Neutral)</i>
<ul>
	<li>Hægt er að nota þvottaduft sem inniheldur engin ylmefni (t.d Neutral) sem gert er fyrir viðkvæma húð</li>
	<li>Ég set það venjulega á langt þvotta-prógram og forþvott</li>
	<li><b>Ekki nota mýkingarefni</b>, það vinnur gegn þér (gerir efnið vatnsfráhrindandi aftur)</li>
</ul>
</li>
	<li>Þurrkið það síðan þegar vélin er búin, á snúru eða í þurrkara.
