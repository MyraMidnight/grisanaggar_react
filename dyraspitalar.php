<h3>Að plata læknirinn</h3>
<p>Naggrísir eru mjög duglegir að fela veikindi, það er innbyggð sjálfsvörn til að koma í veg fyrir að rándýr velji þá sem auðvelda bráð útfrá hegðun. Það gerir það að verkum að naggrísinn þrjóskast til að fela öll einkenni fyrir lækninum í byrjun. Ef dýralæknirinn er þolinmóður og tímir að bíða í 10-15 mínutur, þá er naggrísinn oftast eftir að róa sig og sýnir þá einkenni veikinda sinna.</p>

<h3>Hvernig er hægt að hjálpa naggrís í læknisferð?</h3>
<p>Þú skalt íhuga að taka annan naggrís sem félagskap í ferðina, ef hann er ekki í einangrun vegna smithættu. Það minkar stressið og það hjálpar dýralækninum að geta borið þá saman.</p>
<p>Jafnvel ef dýralæknirinn hefur mikla reynslu á naggrísum, þá hjálpar það þeim rosalega að geta borið hann saman við heilbrigðan naggrís. Þótt það gæti kostað þig aukalega til að taka rönken mynd af þeim báðum, þá flýtir það rosalega fyrir greiningu ef þeir hafa ekki neitt til að miða við.</p>


